export function searchColleges(query: string): string[] {
  if (!query || query.length < 2) return [];

  const lowerQuery = query.toLowerCase();
  return collegesData
    .filter(college =>
      college.toLowerCase().includes(lowerQuery)
    )
    .slice(0, 10); // Limit to 10 suggestions
}